const sum = (num1, num2) => {
    return num1 + num2;
}

const minus = (num1, num2) => {
    return num1 - num2;
}

const munt = (num1, num2) => {
    return num1 * num2;
}

const divide = (num1, num2) => {
    return num1 /  num2;
}

const square = (a) => {
    return a ^ 2;
}

module.exports = {sum, minus, munt, divide, square}